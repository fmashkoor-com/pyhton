#Number System Conversion in Python

#Decimal to binary
#Binary base2 [0-1]
x = bin(25)
print("Convert to Binary = " + str(x))
#Binary to decimanl
x = 0b11001
print("Convert to Decimal = " + str(x))

#Decimal to HexaDecimal
#HexaDecimal base16 [0-9,a-f]
y = hex(25)
print("Convert to HexaDecimal = " + str(y))
#HexaDecimal to Decimal
y = 0x19
print("Convert to Decimal = " + str(y))

#Decimal to Octal
#Octal base8 [0-7]
z = oct(25)
print("Convert to Octa = " + str(z))
#Octal to Decimal
z = 0o31
print("Convert to Decimal = " + str(z))